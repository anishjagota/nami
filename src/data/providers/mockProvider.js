const STATIC_TICKERS = ['SPY', 'AGG', 'VEA', 'VWO', 'GLD', 'VNQ', 'DBC'];

/**
 * Get mock historical prices for a ticker
 * Converts static returns back to synthetic prices for API compatibility
 */
export async function fetchHistoricalPricesMock(ticker) {
  const tickerIdx = STATIC_TICKERS.indexOf(ticker);
  
  if (tickerIdx === -1) {
    // Ticker not in static data
    return [];
  }
  
  // Generate synthetic daily prices from monthly returns
  // Start with base price of 100
  let price = 100;
  const prices = [];
  
  for (let i = 0; i < STATIC_RETURNS.length; i++) {
    const monthReturn = STATIC_RETURNS[i][tickerIdx];
    const date = STATIC_DATES[i];
    
    // Generate month-end price
    const [year, month] = date.split('-').map(Number);
    const lastDayOfMonth = new Date(year, month, 0).getDate();
    const dateStr = `${date}-${lastDayOfMonth.toString().padStart(2, '0')}`;
    
    prices.push({
      date: dateStr,
      adjClose: price,
    });
    
    // Update price for next month
    price = price * (1 + monthReturn);
  }
  
  return prices;
}

/**
 * Get mock returns directly (faster than price conversion)
 */
export function getMockReturns(ticker) {
  const tickerIdx = STATIC_TICKERS.indexOf(ticker);
  
  if (tickerIdx === -1) {
    return null;
  }
  
  const returns = STATIC_RETURNS.map(row => row[tickerIdx]);
  
  return {
    returns,
    dates: STATIC_DATES.slice(0, returns.length),
  };
}

/**
 * Check if a ticker is in the mock data
 */
export function hasMockData(ticker) {
  return STATIC_TICKERS.includes(ticker);
}

/**
 * Get all available mock tickers
 */
export function getMockTickers() {
  return [...STATIC_TICKERS];
}
