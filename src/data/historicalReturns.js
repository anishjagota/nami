/**
 * Get returns for a specific asset (sync, static data only)
 */
export function getAssetReturns(assetId) {
  return staticReturns.returns[assetId] || [];
}

/**
 * Get all dates (sync, static data)
 */
export function getDates() {
  return staticReturns.dates;
}

/**
 * Get returns matrix for multiple assets (sync, static data only)
 * Returns: { dates: string[], returns: number[][] }
 * where returns[i][j] is return of asset j in period i
 * 
 * Note: Only works for assets in the static data set (SPY, AGG, VEA, VWO, GLD, VNQ, DBC)
 */
export function getReturnsMatrix(assetIds) {
  const dates = staticReturns.dates;
  const n = dates.length;
  const returns = [];
  
  // Filter to only assets we have data for
  const validAssetIds = assetIds.filter(id => staticReturns.returns[id]);
  
  for (let i = 0; i < n; i++) {
    const row = validAssetIds.map(id => staticReturns.returns[id][i]);
    returns.push(row);
  }
  
  return { dates, returns };
}

/**
 * Get returns for a date range (sync, static data only)
 */
export function getReturnsInRange(assetIds, startDate, endDate) {
  const dates = staticReturns.dates;
  const startIdx = dates.indexOf(startDate);
  const endIdx = dates.indexOf(endDate);
  
  if (startIdx === -1 || endIdx === -1 || startIdx > endIdx) {
    return { dates: [], returns: [] };
  }
  
  const validAssetIds = assetIds.filter(id => staticReturns.returns[id]);
  const slicedDates = dates.slice(startIdx, endIdx + 1);
  const returns = [];
  
  for (let i = startIdx; i <= endIdx; i++) {
    const row = validAssetIds.map(id => staticReturns.returns[id][i]);
    returns.push(row);
  }
  
  return { dates: slicedDates, returns };
}

